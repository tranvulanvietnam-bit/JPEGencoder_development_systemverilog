    // ------------------------------------------------------------------
    // Clock generation (100 MHz)
    // ------------------------------------------------------------------
    initial clk = 0;
    always #5 clk = ~clk;
    
    // ------------------------------------------------------------------
    // Reset sequence
    // ------------------------------------------------------------------
    initial begin
        reset_n = 0;
        valid_in = 0;
        ready_out = 0;

        #20;
        reset_n = 1;
    end
    
    // ------------------------------------------------------------------
    // Stimulus process
    // ------------------------------------------------------------------
    initial 
    begin
        @(posedge reset_n);
        $display("[%0t] Start simulation", $time);

        // Wait until DUT is ready for input
        @(posedge clk);
        wait (ready_in);

        // Send several RGB values
        send_pixel(R1, G1, B1); 
        
        send_pixel(R2, G2, B2);  
        // Wait for last output
        repeat (20) @(posedge clk);
        $display("[%0t] Simulation completed.", $time);
       $finish;
    end
   
    task send_pixel(input logic [7:0] r [0:63],
                    input logic [7:0] g [0:63],
                    input logic [7:0] b [0:63] );
    begin
        for (int i = 0; i < 64; i++) 
        begin
           R[i] <= r[i];
           G[i] <= g[i];
           B[i] <= b[i];
        end
        valid_in <= 1;
        @(posedge clk);
        valid_in <= 0;

        // Output ready may come with latency
        ready_out <= 0;
        wait (valid_out);
        repeat ($urandom_range(1, 3)) @(posedge clk); // Simulated output latency
        ready_out <= 1;
        @(posedge clk);
        ready_out <= 0;
        for (int i = 0; i < 64; i++) begin
           $display("[%0t] -> Y [%0d] =%0d",
                 $time, i,  Y[i]);
        end
        $display("-----------------------");
    end
    endtask
    
